; copy memory from SRCPTR to DSTPTR
; input:
;       SRCPTR - source address in zeropage + xbyte
;       DSTPTR - destination address in zeropage + xbyte
;       X = pages to move
;
moveblk:    ldy     #0
@m1:        lda     (SRCPTR),Y
            sta     (DSTPTR),Y
            iny
            bne     @m1
            inc     DSTPTR+1
            inc     SRCPTR+1
            dex
            bne     @m1
            rts
