            sta     E_REG
            lda     #$00
            sta     Z_REG        ;Zero page to 00
            sta     B_REG        ;bank 00
                                 ; Note: need to be careful this is below $2000 so we don't
                                 ; get banked out inadvertantly

.if a2mode=1
            ;this enables the hardware A2 emulation mode, commented out for now
