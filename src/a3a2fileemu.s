SETENCWRT   =       $c0db      ;Set Char Set writing

L1401       =       $1401      ;extended addressing page for zp $18

A2RESET     =       $fa62


; org for standard sos.kernel file
            .org    $1E00

; dummy sos.kernel header
            .byte   "SOS KRNL"   ;sos kernel label
