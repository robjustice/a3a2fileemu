            .res    4            ;boot loader assumes $1e0e + length

            lda     #$18         ;setup zero page so we can do extended addressing
            sta     Z_REG
;
; copy AppleSoft and Apple ][ autostart rom to ram d000-ffff using extended addressing
;
            lda     #<rom
            sta     SRCPTR
            lda     #>rom
            sta     SRCPTR+1
            lda     #0
            sta     L1401 + SRCPTR
            sta     DSTPTR
            lda     #$d0
            sta     DSTPTR+1
            lda     #$8f         ;store using special 8f case extended addressing
            sta     L1401 + DSTPTR
            ldx     #$30         ;pages to move
            jsr     moveblk

; copy the slot roms to ram c500-c7ff
; 
