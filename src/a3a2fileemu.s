            and     #$0f
            ora     #$30
            sta     E_PCR
@L0CC5:     lda     #$07
            sta     ENDBLK
@L0CC9:     jsr     L0CFE
            inc     STARTBLK
            lda     SRCPTR
            clc
            adc     #$08
            sta     SRCPTR
            bcc     @L0CD9
            inc     $01
@L0CD9:     dec     ENDBLK
            bpl     @L0CC9
            bit     SETENCWRT    ;enable font loading
            ldx     #$18
            stx     E_IFR
@L0CE5:     lda     E_IFR
            and     #$10
            beq     @L0CE5
            stx     E_IFR
@L0CEF:     lda     E_IFR
            and     #$18
            beq     @L0CEF
            bit     CLRENCWRT    ;disable font loading
            lda     STARTBLK
            bpl     @L0CC5
            rts

L0CFE:      ldx     #$00
            ldy     #$00
@L0D02:     lda     ENDBLK
            and     #$03
            ora     SCRNHOLES,y
            sta     DSTPTR
            lda     ENDBLK
            lsr     A
            lsr     A
            cpy     #$04
            rol     A
            ora     #$08
            sta     $03
            lda     STARTBLK
            sta     (DSTPTR,x)
            lda     $03
            eor     #$0c
            sta     $03
            lda     (SRCPTR),y
            sta     (DSTPTR,x)
            iny
            cpy     #$08
            bcc     @L0D02
            rts

