;
; File based Apple ][ emulation loader for the Apple/// 
;
; This file has headers like a standard sos.kernel file and loaded with the
; normal soshdboot boot loader
;
; This is to provide an environment that will allow the 48k
; Prodos 1.0 to run so that some cc65 based programs
; can be run directly on the Apple ///
;
; Performs the following:
; - Load font embedded in this loader, includes lowercase
; - Copy Applesoft & Autostart roms included in loader to D000-FFFF
; - Copy in Slot roms included in loader to C500-C7FF
; - switch in and write protect the A3 system page ram
; - Setup - bank=0, zeropage=0, 1Mhz, enable Titan ///+II if installed (gives language card) 
; - jump to the Apple2 reset entry point
;
; todo 
; - update the included monitor rom to not clash with the VIAs
;
; By Robert Justice
;
;
            .setcpu "6502"
            .segment "CODE"
;
; build options
;
; Enable apple2 hardware mode
; 0 = funny/satan mode
; 1 = enable A2 hardware emulation mode (need power off to get back to A3 mode)
;
; Note1: Setting funny/Satan mode patches the A2 F8 rom to set the A3 B/W Text mode correctly
; Note2: Funny/Satan mode also allows us to come back to Native A3 mode without a power on/off
;
